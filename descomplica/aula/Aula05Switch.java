package descomplica.aula;

public class Aula05Switch {
    public static void main(String[] args){
        int num = 2;

        switch(num){
            case 1:
                System.out.println("voce digitou 1");
                break;
            case 2:
                System.out.println("voce digitou 2");
                break;
            case 3:
                System.out.println("voce digitou 3");
                break;
            default:
                System.out.println("voce digitou valor invalido.");

        }
    }
}
